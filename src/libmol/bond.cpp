#include <stdlib.h>
#include <stdio.h>
#include <assert.h>

#include <libmol/libmol.h>

namespace LibMol{

void
mol_bond_create (mol_bond* b)
{
	assert (b != NULL);
	return;
}

void
mol_bond_destroy (mol_bond* b)
{
	assert (b != NULL);
	return;
}

void
mol_bond_copy (mol_bond* bsrc, mol_bond* bdst)
{
	assert (bsrc != NULL);
	assert (bdst != NULL);

	bdst->ai = bsrc->ai;
	bdst->aj = bsrc->aj;

	bdst->l = bsrc->l;
	bdst->l0 = bsrc->l0;
	bdst->k = bsrc->k;
}

};
